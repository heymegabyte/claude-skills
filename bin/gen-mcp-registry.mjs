#!/usr/bin/env node
/**
 * gen-mcp-registry.mjs — generate mcp-servers/_registry.json, the single
 * source of truth for the MCP fleet (idea #39). Scans mcp-servers/ and records
 * each server's variant (base|hardened), main entry, and eval count.
 *
 * @example
 * node bin/gen-mcp-registry.mjs   // writes mcp-servers/_registry.json
 */
import { readdirSync, existsSync, writeFileSync, statSync } from 'node:fs';
import { join, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const ROOT = join(dirname(fileURLToPath(import.meta.url)), '..');
const MCP = join(ROOT, 'mcp-servers');

function evalCount(dir) {
  const e = join(dir, 'evals');
  if (!existsSync(e)) return 0;
  return readdirSync(e).filter(f => f.endsWith('.json')).length;
}

function mainEntry(dir) {
  for (const c of ['mcp-server/src/index.ts', 'mcp-server/index.ts', 'src/index.ts']) {
    if (existsSync(join(dir, c))) return c;
  }
  return null;
}

const servers = readdirSync(MCP, { withFileTypes: true })
  .filter(d => d.isDirectory() && !d.name.startsWith('_'))
  .map(d => {
    const dir = join(MCP, d.name);
    const hardened = d.name.includes('-hardened-');
    const base = d.name.replace('-hardened-mcp', '-mcp');
    return {
      name: d.name,
      variant: hardened ? 'hardened' : 'base',
      canonical: hardened, // hardened is the canonical/preferred variant
      supersedes: hardened ? base : null,
      mainEntry: mainEntry(dir),
      evals: evalCount(dir),
    };
  })
  .sort((a, b) => a.name.localeCompare(b.name));

const registry = {
  _comment: 'AUTO-GENERATED by bin/gen-mcp-registry.mjs — source of truth for the MCP fleet. Hardened variants are canonical; base variants are archival candidates once .claude.json is migrated.',
  generatedFrom: 'mcp-servers/',
  count: servers.length,
  servers,
};
writeFileSync(join(MCP, '_registry.json'), JSON.stringify(registry, null, 2) + '\n');
console.log(`Wrote mcp-servers/_registry.json — ${servers.length} servers (${servers.filter(s => s.canonical).length} hardened/canonical, ${servers.filter(s => !s.canonical).length} base/archival-candidate)`);
