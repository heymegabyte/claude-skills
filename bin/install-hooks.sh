#!/usr/bin/env bash
# install-hooks.sh — installs a .git/hooks/pre-commit that runs `npm run lint`.
# Per rules/lint-doctrine.md § Codified incidents (pass-52 pipeline-exit-masking entry).
#
# Idempotent: re-running overwrites the existing hook with current shim.
# Wired into package.json `scripts.prepare` so `npm install` auto-installs it.

set -eo pipefail

REPO_ROOT="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
HOOK="$REPO_ROOT/.git/hooks/pre-commit"

if [ ! -d "$REPO_ROOT/.git" ]; then
  # Running inside a non-git checkout (e.g. tarball install via npm pack). Skip.
  exit 0
fi

cat >"$HOOK" <<'PRECOMMIT'
#!/usr/bin/env bash
# pre-commit hook — runs `npm run lint` against current state.
# Generated by bin/install-hooks.sh. Re-run that script to update.

cd "$(git rev-parse --show-toplevel)" || exit 1
# Use --quiet so info sections compress to 1 line on clean runs (pass-68).
if ! bash bin/lint-all.sh --quiet; then
  echo "" >&2
  echo "✗ pre-commit: npm run lint FAILED — fix gates before committing." >&2
  echo "  To bypass (NOT recommended): git commit --no-verify" >&2
  exit 1
fi
PRECOMMIT

chmod +x "$HOOK"
echo "✓ installed $HOOK"
